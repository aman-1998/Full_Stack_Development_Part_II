package personal.learning.web.view;

public class SchoolTopperSummaryView {
	
	private String iitRollNo;
	
	private String studentName;
	
	private String rank;
	
	private String schoolName;
	
	private String boardShortName;

	public String getIitRollNo() {
		return iitRollNo;
	}

	public void setIitRollNo(String iitRollNo) {
		this.iitRollNo = iitRollNo;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public String getBoardShortName() {
		return boardShortName;
	}

	public void setBoardShortName(String boardShortName) {
		this.boardShortName = boardShortName;
	}
	
}
